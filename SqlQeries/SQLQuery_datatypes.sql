
-- Veri Tipleri ve Örnekler

-- 1. INT: Tamsayý
DECLARE @intExample INT
SET @intExample = 10

-- 2. VARCHAR: Deðiþken uzunlukta karakter dizisi
DECLARE @varcharExample VARCHAR(50)
SET @varcharExample = 'Hello, World!'

-- 3. NVARCHAR: Unicode karakter dizisi
DECLARE @nvarcharExample NVARCHAR(50)
SET @nvarcharExample = N'Unicode Metin'

-- 4. DECIMAL: Ondalýk sayý
DECLARE @decimalExample DECIMAL(10, 2)
SET @decimalExample = 12345.67

-- 5. DATETIME: Tarih ve saat
DECLARE @datetimeExample DATETIME
SET @datetimeExample = '2024-02-06 12:30:00'

-- 6. BIT: Mantýksal (Boolean) deðer
DECLARE @bitExample BIT
SET @bitExample = 1

-- 7. FLOAT: Kayan noktalý sayý
DECLARE @floatExample FLOAT
SET @floatExample = 3.14159

-- 8. MONEY: Para birimi
DECLARE @moneyExample MONEY
SET @moneyExample = 100.50




